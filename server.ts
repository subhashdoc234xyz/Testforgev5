// Orchestrated call helper with retry
async function generateStructuredJSON<T>(
  prompt: string,
  systemInstruction: string,
  schema: any,
  retryAttempt = 1
): Promise<T> {
  if (!process.env.GEMINI_API_KEY) {
    throw new Error("GEMINI_API_KEY is not configured in the secrets panel or environment.");
  }

  try {
    const response = await ai.models.generateContent({
      model: "gemini-2.5-flash",
      contents: prompt,
      config: {
        systemInstruction,
        responseMimeType: "application/json",
        responseSchema: schema,
        temperature: 0.1, // low temperature for precise JSON tasks
      },
    });

    const text = response.text || "";
    try {
      return JSON.parse(text.trim()) as T;
    } catch (parseErr) {
      console.warn(`JSON parsing fail on attempt ${retryAttempt}. Raw Text:`, text);
      if (retryAttempt < 2) {
        console.log("Retrying JSON generation with correction feedback...");
        const correctivePrompt = `${prompt}\n\n[SYSTEM WARNING: Your previous response was not valid JSON. Ensure that your output is ONLY a syntactically correct JSON object or array conforming exactly to the requested Schema. Do not wrap in markdown fences, do not output any other text.]`;
        return await generateStructuredJSON<T>(correctivePrompt, systemInstruction, schema, retryAttempt + 1);
      }
      throw new Error(`Failed to parse AI response as valid JSON: ${(parseErr as Error).message}`);
    }
  } catch (err) {
    throw new Error(`Gemini API Call failed: ${(err as Error).message}`);
  }
}

// Merge response helper implementing lifecycle states
function mergeResponses(
  draftTestCases: any[],
  critiques: any[],
  responses: any[]
): any[] {
  // Map of finalized test cases. Start with clone of drafts marked as final by default,
  // unless they get revised or status is altered below.
  const mergedMap = new Map<string, any>();
  for (const tc of draftTestCases) {
    mergedMap.set(tc.id, { ...tc, status: "final" });
  }

  // Iterate over builder's responses
  for (const resp of responses) {
    const critique = critiques.find((c) => c.id === resp.critiqueId);
    if (!critique) continue;

    // Critique targets an ID
    const targetId = critique.targetTestCaseId;

    if (resp.action === "fixed" && resp.updatedTestCase) {
      if (targetId && targetId !== "null" && targetId !== "") {
        // Revision of a draft test case
        const updated = { ...resp.updatedTestCase, status: "revised" };
        mergedMap.set(targetId, updated);
      } else {
        // Brand new test case created dynamically to cover a requirements gap!
        const newTestCase = { ...resp.updatedTestCase, status: "revised" };
        // If it doesn't already have a valid unique ID or generated a bad one, make sure it has one
        if (!newTestCase.id) {
          const generatedId = `TC-ADD-${Math.floor(100 + Math.random() * 900)}`;
          newTestCase.id = generatedId;
        }
        mergedMap.set(newTestCase.id, newTestCase);
      }
    } else if (resp.action === "rejected") {
      // Critique was rejected, original stands.
      // If the draft testcase is in the map, preserve but ensure its status is "final"
      if (targetId && mergedMap.has(targetId)) {
        const existing = mergedMap.get(targetId);
        mergedMap.set(targetId, { ...existing, status: "final" });
      }
    }
  }

  return Array.from(mergedMap.values());
}

