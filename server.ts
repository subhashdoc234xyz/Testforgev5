// Real Sync Endpoint to UiPath Test Manager via OAuth Client Credentials
app.post("/api/sync-test-case", async (req: Request, res: Response): Promise<void> => {
  const { testCase } = req.body;

  if (!testCase) {
    res.status(400).json({ error: "TestCase payload is required." });
    return;
  }

  const clientId = process.env.UIPATH_CLIENT_ID;
  const clientSecret = process.env.UIPATH_CLIENT_SECRET;
  const org = process.env.UIPATH_ORG;
  const tenant = process.env.UIPATH_TENANT;
  const projectKey = process.env.UIPATH_PROJECT_KEY;

  if (!clientId || !clientSecret || !org || !tenant || !projectKey) {
    const missingKeys = [];
    if (!clientId) missingKeys.push("UIPATH_CLIENT_ID");
    if (!clientSecret) missingKeys.push("UIPATH_CLIENT_SECRET");
    if (!org) missingKeys.push("UIPATH_ORG");
    if (!tenant) missingKeys.push("UIPATH_TENANT");
    if (!projectKey) missingKeys.push("UIPATH_PROJECT_KEY");

    res.status(400).json({
      error: `Missing UiPath configuration: [${missingKeys.join(", ")}]. Please define these keys in the Secrets panel or .env.`,
      configTip: "To complete setup, open settings and define your UiPath External Application credential pair (scopes: 'OR.TestManager' or similar).",
      simulatedFallback: true
    });
    return;
  }

  try {
    console.log("Authenticating with UiPath Identity Service...");
    const tokenUrl = "https://cloud.uipath.com/identity_/connect/token";
    const params = new URLSearchParams();
    params.append("grant_type", "client_credentials");
    params.append("client_id", clientId);
    params.append("client_secret", clientSecret);
    params.append("scope", "OR.TestManager");

    const tokenResponse = await fetch(tokenUrl, {
      method: "POST",
      headers: {
        "Content-Type": "application/x-www-form-urlencoded",
      },
      body: params.toString(),
    });

    if (!tokenResponse.ok) {
      const tokenErrText = await tokenResponse.text();
      throw new Error(`UiPath OAuth Auth Failed: ${tokenResponse.status} - ${tokenErrText}`);
    }

    const tokenData = await tokenResponse.json() as any;
    const accessToken = tokenData.access_token;
    if (!accessToken) {
      throw new Error("Server succeeded but did not return access_token.");
    }

    // Prepare robust markdown metadata for Test Manager layout
    const markdownDescription = `### Vetted by TestForge Adversarial Review

**Preconditions:**
${testCase.preconditions && testCase.preconditions.length > 0
  ? testCase.preconditions.map((p: string) => `- ${p}`).join("\n")
  : "None configured."}

**Test Sequence Steps:**
${testCase.steps && testCase.steps.length > 0
  ? testCase.steps.map((s: any, idx: number) => `${idx + 1}. **Action:** ${s.action}\n   *Expected:* ${s.expectedResult}`).join("\n\n")
  : "None."}

**Labels:**
${testCase.labels && testCase.labels.length > 0 ? testCase.labels.map((l: string) => `#${l}`).join(", ") : "None."}

---
*Created automatically with TestForge dual-agent QA verification. Status: ${testCase.status || 'vetted'}*`;

    const uipathPayload = {
      name: testCase.title,
      description: markdownDescription,
      priority: testCase.priority ? (testCase.priority.charAt(0).toUpperCase() + testCase.priority.slice(1)) : "Medium",
      steps: testCase.steps ? testCase.steps.map((s: any, idx: number) => ({
        index: idx + 1,
        description: s.action,
        expectedResult: s.expectedResult
      })) : []
    };

    const createUrl = `https://cloud.uipath.com/${org}/${tenant}/testmanager_/api/projects/${projectKey}/testcases`;
    console.log(`Sending payload to Test Manager: ${createUrl}`);

    const apiResponse = await fetch(createUrl, {
      method: "POST",
      headers: {
        "Authorization": `Bearer ${accessToken}`,
        "Content-Type": "application/json",
        "Accept": "application/json"
      },
      body: JSON.stringify(uipathPayload),
    });

    if (!apiResponse.ok) {
      const apiErrText = await apiResponse.text();
      throw new Error(`Test Manager API Rejected: ${apiResponse.status} - ${apiErrText}`);
    }

    const resultData = await apiResponse.json() as any;
    const remoteId = resultData.key || resultData.id || `UP-TM-${Math.floor(10000 + Math.random() * 90000)}`;

    res.json({
      success: true,
      remoteId,
      message: "Test case successfully synchronized to UiPath Test Manager!"
    });

  } catch (err) {
    console.error("UiPath Test Manager synchronization error:", err);
    res.status(500).json({
      error: (err as Error).message || "Internal server error connecting to UiPath API.",
      configTip: "Please verify that your client secret & organization parameters are correct and key scopes allow OR.TestManager write access."
    });
  }
});

// Single orchestrated route that client triggers sequentially or as a single endpoint
app.post("/api/generate", async (req: Request, res: Response): Promise<void> => {
  const { requirement, refinementInstruction, previousTestCases } = req.body;

  if (!requirement || requirement.trim() === "") {
    res.status(400).json({ error: "Software requirement is required" });
    return;
  }

  try {
    // Stage 1: Call Builder (round 1)
    let builderRound1Prompt = `Requirement:\n${requirement}`;
    if (refinementInstruction && previousTestCases) {
      builderRound1Prompt += `\n\nRefinement Instruction: ${refinementInstruction}\n\nPrevious active test cases:\n${JSON.stringify(
        previousTestCases
      )}\n\nPlease refine, revise, or generate new test cases conforming to the instruction. Mark any new or updated test cases as status: 'draft'.`;
    }

    const draftTestCases = await generateStructuredJSON<any[]>(
      builderRound1Prompt,
      "You are a senior QA engineer writing test cases for a software requirement. Produce thorough, realistic test cases in the exact JSON schema provided — covering happy path, edge cases, and negative/error cases. Assign realistic priority levels; not everything is critical. Output ONLY valid JSON matching the schema, no prose, no markdown fences.",
      builderRound1ResponseSchema
    );

    // Stage 2: Call Skeptic
    const skepticPrompt = `Requirement:\n${requirement}\n\nProposed drafted test cases:\n${JSON.stringify(
      draftTestCases
    )}`;

    const critiques = await generateStructuredJSON<any[]>(
      skepticPrompt,
      "You are an extremely thorough, skeptical senior QA reviewer. Your ONLY job is to find flaws and gaps in the given test suite relative to the original requirement. Do not be agreeable — assume something was missed and find it. For each issue: if it's a flaw in an EXISTING test case, set targetTestCaseId to that test case's id. If it's something the requirement demands that NO test case covers at all, set targetTestCaseId as an empty string or null and describe what new test case is needed. Be specific — never write a vague critique like 'could be more thorough'; name the exact missing assertion, edge case, or scenario. Output ONLY valid JSON matching the schema, no prose.",
      skepticResponseSchema
    );

    // Stage 3: Call Builder round 2 (Builder Response)
    // If skeptic found nothing, skip straight & finalize drafting cases
    if (!critiques || critiques.length === 0) {
      const finalized = draftTestCases.map((tc) => ({ ...tc, status: "final" }));
      res.json({
        testCases: finalized,
        critiques: [],
        responses: [],
      });
      return;
    }

    // Clean up potentially bad skeptic target IDs
    const validDraftIds = new Set(draftTestCases.map((tc) => tc.id));
    const processedCritiques = critiques.map((c) => {
      // Ensure targetTestCaseId is null/empty if invalid
      if (c.targetTestCaseId && !validDraftIds.has(c.targetTestCaseId)) {
        return { ...c, targetTestCaseId: null };
      }
      return c;
    });

    const builderRound2Prompt = `Requirement:\n${requirement}\n\nDraft test cases:\n${JSON.stringify(
      draftTestCases
    )}\n\nCritique list:\n${JSON.stringify(processedCritiques)}`;

    const responses = await generateStructuredJSON<any[]>(
      builderRound2Prompt,
      "You previously wrote a test suite for this requirement. A reviewer has critiqued it. For EVERY critique listed, you must respond with either 'fixed' (and provide the corrected or newly created test case) or 'rejected' (and explain precisely why the critique doesn't apply — e.g., explicitly out of scope per the requirement). You may not ignore a critique or respond vaguely. If you reject a critique, your explanation must reference the requirement text directly. Output ONLY valid JSON matching the schema, no prose.",
      builderRound2ResponseSchema
    );

    // Merge everything to produce the final test cases
    const mergedTestCases = mergeResponses(draftTestCases, processedCritiques, responses);

    // Mark updated or newly created ones from a refinement pass with a "refinementPassNew" or "new" flag
    let finalPayloadCases = mergedTestCases;
    if (refinementInstruction) {
      const prevIds = new Set((previousTestCases || []).map((p: any) => p.id));
      finalPayloadCases = mergedTestCases.map((tc) => {
        const isNewOrChanged = !prevIds.has(tc.id) || tc.status === "revised";
        if (isNewOrChanged) {
          return { ...tc, isNew: true };
        }
        return tc;
      });
    }

    res.json({
      testCases: finalPayloadCases,
      critiques: processedCritiques,
      responses,
    });
  } catch (err) {
    console.error("Pipeline breakdown error:", err);
    res.status(500).json({
      error: (err as Error).message || "Internal server error during adversarial pipeline run.",
    });
  }
});

// Single step execution routing for user retry-ability
app.post("/api/generate-step", async (req: Request, res: Response): Promise<void> => {
  const { step, requirement, draftTestCases, critiques, refinementInstruction, previousTestCases } = req.body;

  try {
    if (step === "drafting") {
      let builderRound1Prompt = `Requirement:\n${requirement}`;
      if (refinementInstruction && previousTestCases) {
        builderRound1Prompt += `\n\nRefinement Instruction: ${refinementInstruction}\n\nPrevious active test cases:\n${JSON.stringify(
          previousTestCases
        )}\n\nPlease refine, revise, or generate new test cases. Mark any new or updated test cases with status: 'draft'.`;
      }

      const drafts = await generateStructuredJSON<any[]>(
        builderRound1Prompt,
        "You are a senior QA engineer writing test cases. Produce thorough, realistic test cases in the exact JSON schema provided. Output ONLY valid JSON.",
        builderRound1ResponseSchema
      );
      res.json({ draftTestCases: drafts });
      return;
    }

    if (step === "critique") {
      const skepticPrompt = `Requirement:\n${requirement}\n\nProposed drafted test cases:\n${JSON.stringify(
        draftTestCases
      )}`;

      const crits = await generateStructuredJSON<any[]>(
        skepticPrompt,
        "You are an extremely thorough, skeptical senior QA reviewer. Find critical holes and gaps in the drafted test suite relative to the original requirement. Output ONLY valid JSON.",
        skepticResponseSchema
      );
      res.json({ critiques: crits });
      return;
    }

    if (step === "response") {
      const builderRound2Prompt = `Requirement:\n${requirement}\n\nDraft test cases:\n${JSON.stringify(
        draftTestCases
      )}\n\nCritique list:\n${JSON.stringify(critiques)}`;

      const resps = await generateStructuredJSON<any[]>(
        builderRound2Prompt,
        "You written a test suite previously. A reviewer has critiqued it. For EVERY critique listed, respond with either 'fixed' (with corrected/new test case) or 'rejected' explaining why. Output ONLY valid JSON.",
        builderRound2ResponseSchema
      );

      const merged = mergeResponses(draftTestCases, critiques, resps);
      res.json({ responses: resps, testCases: merged });
      return;
    }

    res.status(400).json({ error: `Invalid step type: ${step}` });
  } catch (err) {
    console.error(`Step ${step} failed:`, err);
    res.status(500).json({ error: (err as Error).message });
  }
});

// Setup Vite Dev server or production static serving
async function bootstrapServer() {
  if (process.env.NODE_ENV !== "production") {
    const vite = await createViteServer({
      server: { middlewareMode: true },
      appType: "spa",
    });
    app.use(vite.middlewares);
  } else {
    const distPath = path.join(process.cwd(), "dist");
    app.use(express.static(distPath));
    app.get("*", (req: Request, res: Response) => {
      res.sendFile(path.join(distPath, "index.html"));
    });
  }

  app.listen(PORT, "0.0.0.0", () => {
    console.log(`TestForge full-stack server listening on http://0.0.0.0:${PORT}`);
  });
}

bootstrapServer().catch((err) => {
  console.error("Server bootstrap failed:", err);
});
